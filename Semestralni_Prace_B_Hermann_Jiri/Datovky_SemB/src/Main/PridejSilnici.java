    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean vlozeno = false;
        if (txtNazevSilnice.getText() != null) {
            if (txtMesoOd.getText() != null) {
                if (txtMestoDo.getText() != null) {
                    IMesto mesto1 = new Mesto(txtMesoOd.getText().toString(), null);
                    IMesto mesto2 = new Mesto(txtMestoDo.getText().toString(), null);

                    mestoOd = GUISemA.mapa.najdiMesto(mesto1);
                    mestoDo = GUISemA.mapa.najdiMesto(mesto2);

                    if (mestoOd == null && mestoDo == null) {
                        return;
                    }
                    ISilnice silnice;
                    boolean uzavreno = false;
                    switch (comBoxUzavreno.getSelectedIndex()) {
                        case 1:
                            uzavreno = true;
                            break;
                        case 2:
                            uzavreno = false;
                            break;

                    }

                    silnice = new Silnice(Integer.valueOf(txtOhodnoceni.getText().toString()), uzavreno, txtNazevSilnice.getText().toString(), mestoOd, mestoDo);
                    if (silnice != null) {
                        GUISemA.mapa.pridejHranu(mestoOd, mestoDo, silnice);
                        vlozeno = true;
                    }
                    if (vlozeno == true) {
                        jTextArea1.append("Vlozeno");
                    } else {
                        jTextArea1.append("Nepodarilo se vlozit");
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

