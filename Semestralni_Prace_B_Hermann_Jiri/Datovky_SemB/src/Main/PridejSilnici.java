    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        IMesto pom = new Mesto(txtMesoOd.getText(), null);
        IMesto pom2 = new Mesto(txtMestoDo.getText(), null);
        IMesto mestoOd = GUISemA.mapa.najdiMesto(pom);
        IMesto mestoDo = GUISemA.mapa.najdiMesto(pom2);
        boolean uzavreno = false;
        switch (comBoxUzavreno.getSelectedIndex()) {
            case 1:
                uzavreno = true;
                break;
            case 2:
                uzavreno = false;
                break;

        }
        ISilnice silnice = new Silnice(Integer.valueOf(txtOhodnoceni.getText().toString()), uzavreno, txtNazevSilnice.getText().toString(), mestoOd, mestoDo);

        silnice = GUISemA.mapa.upravSilnic(mestoOd, mestoDo, silnice);
        if (silnice != null) {
            jTextArea1.append("Silnice Upravena : " + silnice.toString() + "\n");
        } else {
            JOptionPane.showMessageDialog(this, "Silnice nebyla nalezena");
        }
    }//GEN-LAST:event_jButton3ActionPerformed


